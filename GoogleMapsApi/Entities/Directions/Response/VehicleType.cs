namespace GoogleMapsApi.Entities.Directions.Response
{
	public enum VehicleType
	{
		/// <summary>
		/// All other vehicles will return this type.
		/// </summary>
		OTHER,

		/// <summary>
		/// Rail.
		/// </summary>
		RAIL,

		/// <summary>
		/// Light rail transit.
		/// </summary>
		METRO_RAIL,

		/// <summary>
		/// Underground light rail.
		/// </summary>
		SUBWAY,

		/// <summary>
		/// Above ground light rail.
		/// </summary>
		TRAM,

		/// <summary>
		/// Monorail.
		/// </summary>
		MONORAIL,

		/// <summary>
		/// Heavy rail.
		/// </summary>
		HEAVY_RAIL,

		/// <summary>
		/// Commuter rail.
		/// </summary>
		COMMUTER_TRAIN,

		/// <summary>
		/// High speed train.
		/// </summary>
		HIGH_SPEED_TRAIN,

		/// <summary>
		/// Bus.
		/// </summary>
		BUS,

		/// <summary>
		/// Intercity bus.
		/// </summary>
		INTERCITY_BUS,

		/// <summary>
		/// Trolleybus.
		/// </summary>
		TROLLEYBUS,

		/// <summary>
		/// Share taxi is a kind of bus with the ability to drop off and pick up passengers anywhere on its route.
		/// </summary>
		SHARE_TAXI,

		/// <summary>
		/// Ferry.
		/// </summary>
		FERRY,

		/// <summary>
		/// A vehicle that operates on a cable, usually on the ground. Aerial cable cars may be of the type GONDOLA_LIFT.
		/// </summary>
		CABLE_CAR,

		/// <summary>
		/// An aerial cable car.
		/// </summary>
		GONDOLA_LIFT,

		/// <summary>
		/// A vehicle that is pulled up a steep incline by a cable. A Funicular typically consists of two cars, with each car acting as a counterweight for the other.
		/// </summary>
		FUNICULAR,

        /// <summary>
        /// Not by google documented vehicle type. But returned by e.g. following directions API call:
        /// https://maps.googleapis.com/maps/api/directions/json?&mode=transit&origin=zurich+airport&destination=brig&departure_time=1534606200
        /// </summary>
        LONG_DISTANCE_TRAIN
    }
}